const findKey = (obj, callback) => {
  let key;
  let isKeyFound = false;
  let objKeys = Object.keys(obj);
  for (let i = 0; i < objKeys.length && !isKeyFound; i++) {
    isKeyFound = callback(obj[objKeys[i]]);
    if (isKeyFound) {
      key = objKeys[i];
    }
  }
  return key;
};

